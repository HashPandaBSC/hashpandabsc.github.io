function getLang() {
    var lang = null
    if (navigator.languages && navigator.languages.length) {
        lang = navigator.languages[0];
    } else {
        lang = navigator.userLanguage || navigator.language || navigator.browserLanguage || 'en';
    }
    return lang.split('-')[0]
}

window.addEventListener('DOMContentLoaded', function(e) {
    var lang = getLang()
    if (translations[lang]) {
        Object.keys(translations[lang]).forEach(function(key) {
            elems = document.querySelectorAll('[data-i18n="'+key+'"]');
            for (var i = 0; i < elems.length; i++) {
                elems[i].innerText = translations[lang][key]
            }
        })
    }
});